    private ArrayList<Tarea> getTareas(Date desde, Date hasta) {
        BDConsulter consulter = new BDConsulter(DATABASE_URL, DATABASE_USER, DATABASE_PASS);//genero la clase, que se conecta a la base postgresql
        consulter.connect();//establezco la conexion

        //Si no hay ningun integrante seleccionado, selecciono todos
        List<String> integrantesSeleccionados = jListIntegrantes.getSelectedValuesList();
        DefaultListModel<String> model = (DefaultListModel<String>) jListIntegrantes.getModel();
        List<String> integrantes = new ArrayList<String>();
        if (integrantesSeleccionados.size() == 0) {
            for (int i = 0; i < model.getSize(); i++) {
                System.out.println((model.getElementAt(i)));
                integrantes.add(model.getElementAt(i));
            }
            integrantesSeleccionados = integrantes;
        }

        ArrayList<Tarea> tareas = new ArrayList<>();
        for (String s : integrantesSeleccionados) {
            tareas.addAll(consulter.getTareas(desde, hasta, (String) jComboBoxProyecto.getSelectedItem(), (String) jComboBoxGrupos.getSelectedItem(), s));//obtengo las tareas creadas entre un rango de fecha dado
        }
        consulter.disconnect();//termino la conexion con la base//termino la conexion con la base
        return tareas;
    }

    private float getPromedio(ArrayList<Tarea> tareas) {
        int horasReales = 0;
        int horasEstimadas = 0;
        for (Tarea t : tareas) {
            horasReales += t.getHorasReales();
            horasEstimadas += t.getHorasEstimadas();
        }
        return (1 - (horasReales / horasEstimadas));
    }

