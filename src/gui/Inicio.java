    private void setGrupos() {
        BDConsulter consulter = new BDConsulter(DATABASE_URL, DATABASE_USER, DATABASE_PASS);//genero la clase, que se conecta a la base postgresql
        consulter.connect();//establezco la conexion
        ArrayList<String> grupos = consulter.getGrupos();
        consulter.disconnect();//termino la conexion con la base//termino la conexion con la base
        for (String s : grupos) {
            jComboBoxGrupos.addItem(s);
        }
    }

    private void setProyectos() {
        BDConsulter consulter = new BDConsulter(DATABASE_URL, DATABASE_USER, DATABASE_PASS);//genero la clase, que se conecta a la base postgresql
        consulter.connect();//establezco la conexion
        ArrayList<String> proyectos = consulter.getProyectos((String) jComboBoxGrupos.getSelectedItem());
        consulter.disconnect();//termino la conexion con la base//termino la conexion con la base
        jComboBoxProyecto.removeAllItems();
        for (String s : proyectos) {
            jComboBoxProyecto.addItem(s);
        }
    }

    private void loadIntegrantes() {
        System.out.println("Loading integrantes");
        BDConsulter consulter = new BDConsulter(DATABASE_URL, DATABASE_USER, DATABASE_PASS);//genero la clase, que se conecta a la base postgresql
        consulter.connect();//establezco la conexion
        ArrayList<String> integrantes = consulter.getIntegrantes((String) jComboBoxGrupos.getSelectedItem(), (String) jComboBoxProyecto.getSelectedItem());
        consulter.disconnect();//termino la conexion con la base//termino la conexion con la base
        //jListIntegrantes.removeAll(); 
        ((DefaultListModel)jListIntegrantes.getModel()).clear();
        System.out.println("REMOVING integ");
        for (String s : integrantes) {
            ((DefaultListModel)jListIntegrantes.getModel()).addElement(s);
        }
    }

